    private void default_mahang(){
        try {
            dbConnection = MySQLConnUtils.getMySQLConnection();
            statement = dbConnection.createStatement();
            String sql = "SELECT * FROM hang,chitietnhap,chitietkho "
                    + "WHERE hang.Ma_hang=chitietnhap.Ma_hang AND hang.Ma_hang=chitietkho.Ma_hang "
                    + "AND hang.Ma_hang='"+cb_mahang.getSelectedItem().toString()+"'";
            
            ResultSet rs = statement.executeQuery(sql);
            while ((rs != null) && (rs.next())) {
                txt_tenhang.setText(rs.getString("Ten_hang"));
                txt_tonkho.setText(rs.getString("SL_tonkho"));
                txt_dongia.setText(rs.getString("Giaban"));
            }
        }
        catch(Exception e ){
            JOptionPane.showMessageDialog(null, e);
        }  
    }
