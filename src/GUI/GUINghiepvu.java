    private void ctietX(){
        Nghiepvu nghiepvu = new Nghiepvu();
        boolean isNumeric = true;
        for (int i = 0; i < tbl_data.getRowCount() ; i++) {
            try {
                int num = Integer.parseInt(tbl_data.getValueAt(i, 3).toString());
//                int num2=Integer.parseInt(txt_mahoadon.getText());
                isNumeric = true;

             } catch (NumberFormatException e) {
                isNumeric = false;
            }
            if (isNumeric) {
                try {
                    Connection conn = MySQLConnUtils.getMySQLConnection();
                    PreparedStatement stm = conn.prepareStatement("SELECT * FROM `hoadonxuat` ORDER BY hoadonxuat.Ma_HDX DESC LIMIT 0,1");
                    ResultSet rs = stm.executeQuery();

                    while (rs.next()) {
                        nghiepvu.setMa_HDX(rs.getInt("Ma_HDX"));
                    }
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(DAONghiepvu.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(DAONghiepvu.class.getName()).log(Level.SEVERE, null, ex);
                }
                nghiepvu.setSoluongX(Integer.parseInt(tbl_data.getValueAt(i, 3).toString()));
                nghiepvu.setGiam_gia(Integer.parseInt(tbl_data.getValueAt(i, 4).toString()));
                nghiepvu.setMa_hang(tbl_data.getValueAt(i , 0).toString());
                
                DAOnghiepvu.ThemCTxuat(nghiepvu); 
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Số hóa đơn phải nhập số!", "Thông báo", JOptionPane.WARNING_MESSAGE); 
            }
        }
    }    
