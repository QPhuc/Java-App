    public void loadData() {
        // Clear table
        tbl_data.setModel(new DefaultTableModel());
        // Model for Table
        DefaultTableModel model = new DefaultTableModel() {
            public Class<?> getColumnClass(int column) {
                switch (column) {
                    case 0:
                        return String.class;
                    case 1:
                        return String.class;
                    case 2:
                        return String.class;
                    case 3:
                        return String.class;
                    case 4:
                        return Boolean.class;
                    default:
                        return String.class;
                }
            }
        };
        tbl_data.setModel(model);

        // Add Column
        model.addColumn("STT");
        model.addColumn("Tài Khoản");
        model.addColumn("Mật Khẩu");
        model.addColumn("Quyền");
        model.addColumn("Chọn");

        String sql = "SELECT * FROM  nguoidung";
        try {
            dbConnection = MySQLConnUtils.getMySQLConnection();
            statement = dbConnection.createStatement();
            ResultSet rs = statement.executeQuery(sql);

            int row = 0;
            while ((rs != null) && (rs.next())) {
                model.addRow(new Object[0]);
                model.setValueAt(row + 1, row, 0);
                model.setValueAt(rs.getString("Taikhoan"), row, 1);
                model.setValueAt(rs.getString("Matkhau"), row, 2);
                String quyen;
                if (rs.getString("quyen").equals("0")) {
                    quyen = "Admin";
                } else {
                    //nếu giá trị gioitinh khác 1 và 0 thì sẽ hiện lên là khác
                    quyen = "Nhân viên";
                }
                model.setValueAt(quyen, row, 3);
                model.setValueAt(false, row, 4); // Checkbox

                row++;
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

