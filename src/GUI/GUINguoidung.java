    private void tbl_dataMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_dataMousePressed
        int row = tbl_data.getSelectedRow();
        String tk = tbl_data.getValueAt(row, 1).toString();
        Nguoidung nguoidung = new Nguoidung();
        nguoidung = DAOnguoidung.TimNguoidung(tk);
        txttaikhoan.setText(tk);
        txtmatkhau.setText(nguoidung.getMatkhau());
         if (nguoidung.getquyen()== 0) {
            r_admin.setSelected(true);
        }else {
            r_nhanvien.setSelected(true);
        }
    }//GEN-LAST:event_tbl_dataMousePressed

    private void btnsuaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnsuaMouseClicked
        Nguoidung nguoidung = new Nguoidung();
        nguoidung.setMatkhau(txtmatkhau.getText());
        nguoidung.setTaikhoan(txttaikhoan.getText());
        if (buttonGroup1.getSelection().getActionCommand().equals("Admin")) {
            nguoidung.setquyen(0);
        }else {
            nguoidung.setquyen(1);
        }
        DAOnguoidung.SuaNguoidung(nguoidung);
        txttaikhoan.setEnabled(true);
        JOptionPane.showMessageDialog(this, "Thông tin người dùng đã được cập nhật!","Thông báo",JOptionPane.WARNING_MESSAGE);
        reset();
        loadData();
    }//GEN-LAST:event_btnsuaMouseClicked

