    private void tbl_dataMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_dataMousePressed
        DefaultTableModel model = (DefaultTableModel)tbl_data.getModel();
        int row = tbl_data.getSelectedRow();
        String ma_nv = tbl_data.getValueAt(row, 0).toString();
        Danhmuc danhmuc = new Danhmuc();
        danhmuc = DAOdanhmuc.TimNhanvien(ma_nv);
        txtma_nv.setText(ma_nv);
        txthoten.setText(danhmuc.getHoten());
        txtdiachi.setText(danhmuc.getDiachi());
        if (danhmuc.getGioitinh() == 0) {
            r_nu.setSelected(true);
        } else if (danhmuc.getGioitinh() == 1) {
            r_nam.setSelected(true);
        } else {
            r_khac.setSelected(true);
        }
        txtsdt.setText(danhmuc.getSDT().toString());
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String)model.getValueAt(row, 4));
            cbngaysinh.setDate(date);
        } catch (Exception ex) {
           
        }
        txtma_nv.setEditable(false);
    }//GEN-LAST:event_tbl_dataMousePressed

