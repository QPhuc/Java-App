    public void loadData() {
    // Clear table
    tbl_data.setModel(new DefaultTableModel());
    // Model for Table
    DefaultTableModel model = new DefaultTableModel() {
        public Class<?> getColumnClass(int column) {
            switch (column) {
            case 0:
                return String.class;
            case 1:
                return String.class;
            case 2:
                return String.class;
            case 3:
                return String.class;
            case 4:
                return String.class;
            case 5:
                return String.class;
            case 6:
                return Boolean.class;
            default:
                return String.class;        
          }
        }
    };
    tbl_data.setModel(model);

    // Add Column
    model.addColumn("Mã Nhân Viên");
    model.addColumn("Tên Nhân Viên");
    model.addColumn("Địa Chỉ");
    model.addColumn("Giới Tính");
    model.addColumn("Ngày Sinh");
    model.addColumn("Số Điện Thoại");
    model.addColumn("Chọn");
    String sql = "SELECT * FROM  nhanvien";
    try {
        dbConnection = MySQLConnUtils.getMySQLConnection();
        statement = dbConnection.createStatement();
        ResultSet rs = statement.executeQuery(sql);

        int row = 0;
        while ((rs != null) && (rs.next())) {
            model.addRow(new Object[0]);
            model.setValueAt(rs.getString("Ma_NV"), row, 0);
            model.setValueAt(rs.getString("Hoten"), row, 1);
            model.setValueAt(rs.getString("Diachi"), row, 2);
            
            String gioitinh;
            if (rs.getString("Gioitinh").equals("0")) {
                gioitinh = "Nữ";
            } else if (rs.getString("Gioitinh").equals("1")) {
                gioitinh = "Nam";
            } else {
               //nếu giá trị gioitinh khác 1 và 0 thì sẽ hiện lên là khác
                gioitinh = "Khác";
            }
            model.setValueAt(gioitinh, row, 3);

            
            model.setValueAt(rs.getString("Ngaysinh"), row, 4);
            model.setValueAt("0"+rs.getString("SDT"), row, 5);
            model.setValueAt(false, row, 6); // Checkbox
            
            row++;
            }
        } catch (Exception e) {
            e.printStackTrace();
       }
    }
    
