    private void btnthemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnthemMouseClicked
        boolean isNumeric = true;
        
        try {
            int num = Integer.parseInt(txtsdt.getText());
            isNumeric = true;

         } catch (NumberFormatException e) {
            isNumeric = false;
        }
        if (isNumeric) {
            Danhmuc danhmuc = new Danhmuc();
            danhmuc.setMa_NV(txtma_nv.getText());
            danhmuc.setHoten(txthoten.getText());

            if (buttonGroup1.getSelection() != null) {
                if (buttonGroup1.getSelection().getActionCommand().equals("Nữ")) {
                    danhmuc.setGioitinh(0);
                } else if (buttonGroup1.getSelection().getActionCommand().equals("Nam")) {
                    danhmuc.setGioitinh(1);
                } else {
                    danhmuc.setGioitinh(2);
                }
            } else {
                //Nếu người dùng không chọn ô button nào thì gioitinh sẽ để mặc định là 2
                 danhmuc.setGioitinh(2);
            }    
            danhmuc.setDiachi(txtdiachi.getText());

            Date date = cbngaysinh.getDate();
            String dateInput = new SimpleDateFormat("yyyy-MM-dd").format(date);
            danhmuc.setNgaysinh(dateInput);

            danhmuc.setSDT(Integer.parseInt(txtsdt.getText()));

            if(DAOdanhmuc.ThemNhanvien(danhmuc)){
                if (txtma_nv.getText().equals("") || txthoten.getText().equals("")) {
                    //nếu 1 số ô trong đó để rỗng sẽ đưa ra thông báo
                    JOptionPane.showMessageDialog(null, "Bạn chưa nhập đủ thông tin!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                }else {
                    //ngược lại thực hiện lấy dữ liệu trong các ô để đưa vào CSDL
                     DAOdanhmuc.ThemNhanvien(danhmuc);

                    JOptionPane.showMessageDialog(this, "Thêm mới Nhân viên thành công!");//đưa ra thông báo khi thực hiện thành công
                    loadData();//load lại form
                    reset();//xóa dữ liệu trong các ô text
                    txtma_nv.setEditable(true);
                }
            }else{
            JOptionPane.showMessageDialog(this, "Mã số nhân viên đã bị trùng!", "Lỗi", JOptionPane.WARNING_MESSAGE);
            loadData();
            reset();
        }
 
        }
            else {
                JOptionPane.showMessageDialog(this, "SDT của nhân viên phải nhập số!", "Thông báo", JOptionPane.WARNING_MESSAGE); 
            }
    }//GEN-LAST:event_btnthemMouseClicked

