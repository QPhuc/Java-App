    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
       boolean isNumeric = true;
        
        try {
            int num = Integer.parseInt(txt_sdt.getText());
            isNumeric = true;

        } catch (NumberFormatException e) {
            isNumeric = false;
        }
        if (isNumeric) { 
            Dm_khachhang khachhang = new Dm_khachhang();
            khachhang.setTen_KH(txt_tenkh.getText());
            khachhang.setDiachi(txt_diachi.getText());
            khachhang.setSDT(Integer.parseInt(txt_sdt.getText()));

                if (txt_sdt.getText().equals("") || txt_tenkh.getText().equals("")) {
                    //nếu 1 số ô trong đó để rỗng sẽ đưa ra thông báo
                    JOptionPane.showMessageDialog(null, "Bạn chưa nhập đủ thông tin!", "Thông báo", JOptionPane.WARNING_MESSAGE);
                } else {
                    //ngược lại thực hiện lấy dữ liệu trong các ô để đưa vào CSDL
                    DAOkhachhang.ThemKhachhang(khachhang);

                    JOptionPane.showMessageDialog(this, "Thêm mới Khách hàng thành công!");//đưa ra thông báo khi thực hiện thành công
                    loadData();//load lại form
                    reset();//xóa dữ liệu trong các ô text
                }

            
        }else{
            JOptionPane.showMessageDialog(null,"Số Điện Thoại của Khách hàng phải nhập số!");
        }
    }//GEN-LAST:event_jPanel2MouseClicked

    private void jPanel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MouseClicked
        Integer confirm = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa các khách hàng này không?", "Xóa", 2);
        //khi người dùng ấn đồng ý
        if (confirm == JOptionPane.YES_OPTION) {

            for (int i = 0; i < tbl_data.getRowCount(); i++) {
                Boolean chkDel = Boolean.valueOf(tbl_data.getValueAt(i, 6).toString()); // Checked
                if(chkDel){
                    String kh = tbl_data.getValueAt(i, 1).toString();

                    DAOkhachhang.XoaKhachhang(kh); // Delete Data
                    reset();
                    
                }
            }
            JOptionPane.showMessageDialog(this, "Xóa dữ liệu thành công!");
            loadData(); // Reload Table
        }else{
            reset();
        }

   
    }//GEN-LAST:event_jPanel3MouseClicked

