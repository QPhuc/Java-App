    public void loadData() {
    // Clear table
    tbl_data.setModel(new DefaultTableModel());
    // Model for Table
    DefaultTableModel model = new DefaultTableModel() {
        public Class<?> getColumnClass(int column) {
            switch (column) {
            case 0:
                return String.class;
            case 1:
                return String.class;
            case 2:
                return String.class;
            case 3:
                return String.class;
            case 4:
                return String.class;
            case 5:
                return String.class;
            case 6:
                return Boolean.class;
            default:
                return String.class;        
          }
        }
    };
    tbl_data.setModel(model);

    // Add Column
    model.addColumn("STT");
    model.addColumn("Mã Khách Hàng");
    model.addColumn("Tên Khách Hàng");
    model.addColumn("Địa Chỉ");
    model.addColumn("SĐT");
    model.addColumn("Nợ Đầu");
    model.addColumn("Chọn");
    
    String sql = "SELECT * FROM  khachhang";
    try {
        dbConnection = MySQLConnUtils.getMySQLConnection();
        statement = dbConnection.createStatement();
        ResultSet rs = statement.executeQuery(sql);

        int row = 0;
        while ((rs != null) && (rs.next())) {
            model.addRow(new Object[0]);
            model.setValueAt(row+1, row, 0);
            model.setValueAt(rs.getString("Ma_KH"), row, 1);
            model.setValueAt(rs.getString("Ten_KH"), row, 2);
            model.setValueAt(rs.getString("Diachi"), row, 3);
            model.setValueAt("0"+rs.getString("SDT"), row, 4);
            model.setValueAt(rs.getString("No_dau"), row, 5);
            
                
            model.setValueAt(false, row, 6); // Checkbox
            
            row++;
            }
        } catch (Exception e) {
            e.printStackTrace();
       }
    }
